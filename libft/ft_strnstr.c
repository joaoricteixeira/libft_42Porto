#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	if (*little == '\0')
		return ((char *)big);
	i = 0;
	while (big[i] != '\0' && i < len)
	{
		j = 0;
		while (big[i + j] != '\0' && little[j] != '\0'
			&& i + j < len && big[i + j] == little[j])
			j++;
		if (little[j] == '\0')
			return ((char *)(big + i));
		i++;
	}
	return (NULL);
}
/*the function locates the first occurance
* of the null-terminated string little in the
* string big, where not more than len characters
* are searched. Characters that appear after '\0'
* characters are not searched.
* If little is an empty string, big is returned;
* If little occurs nowhere in big, NULL is returned;
* Otherwhise a pointer to the first character of
* the first occurance little is returned*/
